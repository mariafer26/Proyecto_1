package EarthBeing;

public abstract class LivingBeing {

    public void born() {
        System.out.println("Born...");
    }
    public void breathe() {
        System.out.println("Breathing...");
    }
    public void grow() {
        System.out.println("Growing...");
    }
    public void reproduce() {
        System.out.println("Reproduce...");
    }
    public void die() {
        System.out.println("Diying...");
    }

}
